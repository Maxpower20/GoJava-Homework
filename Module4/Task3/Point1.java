public class Point1 {
    private double x1;
    private double y1;

    Point1(double x1, double y1){
        this.x1 = x1;
        this.y1 = y1;
    }

    public double getX1() {
        return this.x1;
    }

    public void setX1(double x1) {
        this.x1 = x1;
    }

    public void setY1(double y1) {
        this.y1 = y1;
    }

    public double getY1() {
        return this.y1;
    }
}

